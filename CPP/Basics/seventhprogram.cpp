// arithmetic operators
#include <iostream>

using namespace std;
int main(){
    int x = 5, y = 2;
    cout<<x-y<<endl;
    cout<<x+y<<endl;
    cout<<x*y<<endl;
    cout<<x/y<<endl;   // here output is 2 because x and y is integer

    float a = 5, b = 2;
    cout<<a/b; // here output is 2.5 because variable is float data type
}